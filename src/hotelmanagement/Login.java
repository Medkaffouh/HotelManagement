    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String email = jTextField1.getText().trim();
        String pwd = jPasswordField1.getText().trim();
        try {
            Connection con = oc.getConnect();
            String sql1 = "select * from users where email=lower(?)";
            PreparedStatement pst1 = con.prepareStatement(sql1);
            pst1.setString(1, email);
            ResultSet rs1 = pst1.executeQuery();
            if (rs1.next()) {
                jLabel6.setText("");
                jLabel11.setIcon(new ImageIcon(""));
                String sql2 = "select * from users where password=?";
                PreparedStatement pst2 = con.prepareStatement(sql2);
                pst2.setString(1, pwd);
                ResultSet rs2 = pst2.executeQuery();
                if (rs2.next()) {
                    jLabel7.setText("");
                    jLabel12.setIcon(new ImageIcon(""));
                    String sql3 = "select * from users where email=lower(?) and password=?";
                    PreparedStatement pst3 = con.prepareStatement(sql3);
                    pst3.setString(1, email);
                    pst3.setString(2, pwd);
                    ResultSet rs3 = pst3.executeQuery();
                    if (rs3.next()) {
                        dispose();
                        Home h = new Home();
                        h.setVisible(true);
                    }
                } else {
                    jLabel7.setText("mot de passe incorrect");
                    jLabel12.setIcon(new ImageIcon("src\\Icons\\error.png"));
                }
            } else {
                jLabel6.setText("e-mail incorrect ou n'existe pas");
                jLabel11.setIcon(new ImageIcon("src\\Icons\\error.png"));
            }
            isactive();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

